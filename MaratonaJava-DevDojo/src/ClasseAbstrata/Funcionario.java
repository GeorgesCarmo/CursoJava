package ClasseAbstrata;

/**
 *
 * @author luana
 */
public abstract class Funcionario extends Pessoa { // Funcionario é uma classe abstrata então não pode ser instanciada, somente herdada

    protected String nome;
    protected double salario;

    public Funcionario(String nome, double salario) {
        this.nome = nome;
        this.salario = salario;
        calculaBonus();
    }
public abstract void calculaBonus();
}
